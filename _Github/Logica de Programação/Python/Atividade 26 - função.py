
def soma(n1, n2):
    return(n1+n2)
def subtracao(n1, n2):
    return(n1-n2)
def multiplicacao(n1, n2):
    return(n1*n2)
def divisao(n1, n2):
    return(n1/n2)
print('--------soma----------')
print(soma(45, 55))

print("--------subtração----------")
print(subtracao(150,50))

print('--------multiplicação----------')
print(multiplicacao(10, 10))

print('--------divisão----------')
print(divisao(1000 , 10))
